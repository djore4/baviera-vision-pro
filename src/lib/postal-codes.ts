export function postalCodeToLatLng(postalCode: string): [number, number] | null {
  if (!postalCode) return null;
  const clean = postalCode.replace(/[^0-9]/g, '');
  if (clean.length < 2) return null;
  
  const prefix = clean.substring(0, 2);
  const base = PT_POSTAL_MAP[prefix];
  if (!base) return null;
  
  // Add small random offset to avoid overlapping
  const jitter = () => (Math.random() - 0.5) * 0.02;
  return [base[0] + jitter(), base[1] + jitter()];
}
